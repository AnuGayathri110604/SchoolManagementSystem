private void refreshTable() {
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sms?useUnicode=true&useJDBCCompliantTimezoneShift=true&useLegacyDatetimeCode=false&serverTimezone=UTC", "root", "");

        Statement st = conn.createStatement();
        String sql = "select * from stureg";
        PreparedStatement ptst = conn.prepareStatement(sql);
        ResultSet rs = ptst.executeQuery();
        DefaultTableModel tm = (DefaultTableModel) studTable.getModel();
        tm.setRowCount(0);
        while (rs.next()) {
            Object o[] = {rs.getInt("ID"), rs.getString("Name"), rs.getString("fname"), rs.getString("phone"),
                rs.getString("fatherphone"), rs.getString("class"), rs.getString("roll"), rs.getString("address")};
            tm.addRow(o);
        }
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
