    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String sd = sid.getText();
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sms?useUnicode=true&useJDBCCompliantTimezoneShift=true&useLegacyDatetimeCode=false&serverTimezone=UTC", "root", "");

        // Fetch student details using the provided ID
        String fetchSql = "SELECT * FROM stureg WHERE ID = ?";
        PreparedStatement fetchStmt = conn.prepareStatement(fetchSql);
        fetchStmt.setString(1, sd);
        ResultSet fetchResultSet = fetchStmt.executeQuery();

        if (fetchResultSet.next()) {
            String details = "ID: " + fetchResultSet.getInt("ID") +
                            "\nName: " + fetchResultSet.getString("Name") +
                            "\nFather's Name: " + fetchResultSet.getString("fname") +
                            "\nPhone: " + fetchResultSet.getString("phone") +
                            "\nFather's Phone: " + fetchResultSet.getString("fatherphone") +
                            "\nClass: " + fetchResultSet.getString("class") +
                            "\nRoll No: " + fetchResultSet.getString("roll") +
                            "\nAddress: " + fetchResultSet.getString("address");

            // Display the details and confirm deletion
            int choice = JOptionPane.showConfirmDialog(null, "ARE YOU SURE YOU WANT TO DELETE THIS RECORD?\n" + details, "Delete Record", JOptionPane.YES_NO_OPTION);

            if (choice == JOptionPane.YES_OPTION) {
                // Delete the record using the provided ID
                String deleteSql = "DELETE FROM stureg WHERE ID = ?";
                PreparedStatement deleteStmt = conn.prepareStatement(deleteSql);
                deleteStmt.setString(1, sd);
                deleteStmt.executeUpdate();

                // Delete the corresponding record from reportcard table
                deleteReportCardRecord(sd);

                // Close the connection after deletion
                conn.close();
                sid.setText("");

                // Refresh and display records in the table
                refreshTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "No student record found for the provided ID.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jButton2ActionPerformed
